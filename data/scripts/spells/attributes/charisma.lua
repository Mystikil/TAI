local spell = Spell(SPELL_INSTANT)
function spell.onCastSpell(creature, variant)
        creature:sendTextMessage(MESSAGE_INFO_DESCR, "Charisma: " .. creature:getCharisma())
        return true
end
spell:group("support")
spell:id(306)
spell:name("Check Charisma")
spell:words("exiva cha")
spell:level(1)
spell:mana(0)
spell:isAggressive(false)
spell:isSelfTarget(true)
spell:cooldown(1000)
spell:groupCooldown(1000)
spell:vocation("sorcerer;true", "druid;true", "paladin;true", "knight;true", "master sorcerer;true", "elder druid;true", "royal paladin;true", "elite knight;true")
spell:register()
